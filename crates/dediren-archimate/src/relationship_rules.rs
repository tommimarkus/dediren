use crate::{
    validate_element_type, validate_relationship_type, ArchimateTypeKind,
    ArchimateTypeValidationError,
};

// Source checked on 2026-05-12:
// The Open Group ArchiMate 3.2 Specification, Section 5.7 and Appendix B.5
// relationship tables, from the local PDF provided for this task. This module
// intentionally supports ArchiMate 3.2 only.

#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord)]
pub struct RelationshipEndpointTriple {
    pub source_type: &'static str,
    pub relationship_type: &'static str,
    pub target_type: &'static str,
}

pub fn validate_relationship_endpoint_types(
    relationship_type: &str,
    source_type: &str,
    target_type: &str,
    path: impl Into<String>,
) -> Result<(), ArchimateTypeValidationError> {
    let path = path.into();
    validate_relationship_type(relationship_type, path.clone())?;
    validate_element_type(source_type, path.clone())?;
    validate_element_type(target_type, path.clone())?;

    if is_relationship_endpoint_allowed(relationship_type, source_type, target_type) {
        Ok(())
    } else {
        Err(ArchimateTypeValidationError {
            kind: ArchimateTypeKind::RelationshipEndpoint,
            value: format!("{source_type} -[{relationship_type}]-> {target_type}"),
            path,
        })
    }
}

pub fn relationship_endpoint_triples() -> Vec<RelationshipEndpointTriple> {
    let mut triples = Vec::new();
    for (source_type, target_codes) in SOURCE_RELATIONSHIP_CODES {
        for (target_index, target_type) in TARGET_TYPES.iter().enumerate() {
            triples.push(RelationshipEndpointTriple {
                source_type,
                relationship_type: "Association",
                target_type,
            });

            for code in target_codes[target_index].chars() {
                triples.push(RelationshipEndpointTriple {
                    source_type,
                    relationship_type: relationship_name_for_code(code)
                        .expect("relationship code table must use supported codes"),
                    target_type,
                });
            }
        }
    }
    triples
}

fn is_relationship_endpoint_allowed(
    relationship_type: &str,
    source_type: &str,
    target_type: &str,
) -> bool {
    if relationship_type == "Association" {
        return true;
    }

    let Some(code) = relationship_code(relationship_type) else {
        return false;
    };
    let Some(target_index) = TARGET_TYPES.iter().position(|name| *name == target_type) else {
        return false;
    };
    let Some((_, target_codes)) = SOURCE_RELATIONSHIP_CODES
        .iter()
        .find(|(name, _)| *name == source_type)
    else {
        return false;
    };

    target_codes[target_index].contains(code)
}

fn relationship_code(relationship_type: &str) -> Option<char> {
    match relationship_type {
        "Access" => Some('a'),
        "Composition" => Some('c'),
        "Flow" => Some('f'),
        "Aggregation" => Some('g'),
        "Assignment" => Some('i'),
        "Influence" => Some('n'),
        "Association" => Some('o'),
        "Realization" => Some('r'),
        "Specialization" => Some('s'),
        "Triggering" => Some('t'),
        "Serving" => Some('v'),
        _ => None,
    }
}

fn relationship_name_for_code(code: char) -> Option<&'static str> {
    match code {
        'a' => Some("Access"),
        'c' => Some("Composition"),
        'f' => Some("Flow"),
        'g' => Some("Aggregation"),
        'i' => Some("Assignment"),
        'n' => Some("Influence"),
        'o' => Some("Association"),
        'r' => Some("Realization"),
        's' => Some("Specialization"),
        't' => Some("Triggering"),
        'v' => Some("Serving"),
        _ => None,
    }
}

